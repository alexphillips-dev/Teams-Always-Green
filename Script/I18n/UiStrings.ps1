function Normalize-UiStrings {
    if (-not ($script:UiStrings -is [hashtable])) { return }
    $base = $script:UiStrings["en"]
    if (-not ($base -is [hashtable])) { return }
    foreach ($langKey in $script:UiStrings.Keys) {
        if ($langKey -eq "en") { continue }
        $table = $script:UiStrings[$langKey]
        if (-not ($table -is [hashtable])) {
            $table = @{}
            $script:UiStrings[$langKey] = $table
        }
        foreach ($key in $base.Keys) {
            if (-not $table.ContainsKey($key)) {
                $table[$key] = $base[$key]
            }
        }
    }
}
Normalize-UiStrings

$script:UiReverse = @{}
foreach ($langKey in $script:UiStrings.Keys) {
    $table = $script:UiStrings[$langKey]
    if (-not ($table -is [hashtable])) { continue }
    foreach ($key in $table.Keys) {
        $value = [string]$table[$key]
        if ([string]::IsNullOrWhiteSpace($value)) { continue }
        if (-not $script:UiReverse.ContainsKey($value)) {
            $script:UiReverse[$value] = $key
        }
    }
}

$script:UiLanguage = "en"
$script:UiStringCache = @{}
$script:UiStringCacheLang = $null

function Resolve-UiLanguage([string]$value) {
    $resolved = $value
    if ([string]::IsNullOrWhiteSpace($resolved) -or $resolved -eq "auto") {
        $resolved = [System.Globalization.CultureInfo]::CurrentUICulture.TwoLetterISOLanguageName
    }
    if (-not $script:UiStrings.ContainsKey($resolved)) { $resolved = "en" }
    return $resolved
}

function L([string]$key, [string]$fallback = $null) {
    if ([string]::IsNullOrWhiteSpace($key)) { return "" }
    $lang = if ($script:UiLanguage) { $script:UiLanguage } else { "en" }
    if ($script:UiStringCacheLang -ne $lang) {
        $script:UiStringCache = @{}
        $script:UiStringCacheLang = $lang
    }
    $cacheKey = "$lang|$key"
    if ($script:UiStringCache.ContainsKey($cacheKey)) {
        return $script:UiStringCache[$cacheKey]
    }
    $result = $null
    if ($script:UiStrings.ContainsKey($lang) -and $script:UiStrings[$lang].ContainsKey($key)) {
        $result = $script:UiStrings[$lang][$key]
    } elseif ($script:UiStrings.ContainsKey("en") -and $script:UiStrings["en"].ContainsKey($key)) {
        $result = $script:UiStrings["en"][$key]
    } elseif ($fallback -ne $null) {
        $result = $fallback
    } else {
        $result = $key
    }
    $script:UiStringCache[$cacheKey] = $result
    return $result
}

function Localize-StatusValue([string]$value) {
    if ([string]::IsNullOrWhiteSpace($value)) { return $value }
    return (L $value)
}

function Localize-ControlTree($root) {
    if (-not $root) { return }
    $translateControl = {
        param($control)
        if (-not $control) { return }
        $skip = $control -is [System.Windows.Forms.TextBox] -or
            $control -is [System.Windows.Forms.ComboBox] -or
            $control -is [System.Windows.Forms.NumericUpDown] -or
            $control -is [System.Windows.Forms.DateTimePicker]
        if (-not $skip -and $control.PSObject.Properties.Name -contains "Text") {
            $tagText = $null
            if ($control.Tag -is [string] -and -not [string]::IsNullOrWhiteSpace($control.Tag)) {
                if ($control.Tag -notlike "Header:*") {
                    $tagText = $control.Tag
                }
            } elseif ($script:UiStrings.ContainsKey("en") -and $script:UiStrings["en"].ContainsKey([string]$control.Text)) {
                $control.Tag = [string]$control.Text
                $tagText = $control.Tag
            } elseif ($script:UiReverse.ContainsKey([string]$control.Text)) {
                $control.Tag = [string]$script:UiReverse[[string]$control.Text]
                $tagText = $control.Tag
            }
            $key = if ($tagText) { $tagText } else { $control.Text }
            $translated = L $key
            if (-not [string]::IsNullOrWhiteSpace($translated)) {
                $control.Text = $translated
            }
        }
    }
    $translateControl.Invoke($root)
    foreach ($child in $root.Controls) {
        Localize-ControlTree $child
    }
}

function Localize-MenuItems($items) {
    if (-not $items) { return }
    foreach ($item in $items) {
        if ($item -is [System.Windows.Forms.ToolStripItem]) {
            $tagText = $null
            if ($item.Tag -is [string] -and -not [string]::IsNullOrWhiteSpace($item.Tag)) {
                $tagText = $item.Tag
            } elseif ($script:UiStrings.ContainsKey("en") -and $script:UiStrings["en"].ContainsKey([string]$item.Text)) {
                $item.Tag = [string]$item.Text
                $tagText = $item.Tag
            } elseif ($script:UiReverse.ContainsKey([string]$item.Text)) {
                $item.Tag = [string]$script:UiReverse[[string]$item.Text]
                $tagText = $item.Tag
            }
            $key = if ($tagText) { $tagText } else { $item.Text }
            if ($key) {
                $translated = L $key
                if (-not [string]::IsNullOrWhiteSpace($translated)) {
                    $item.Text = $translated
                }
            }
        }
        if ($item -is [System.Windows.Forms.ToolStripMenuItem] -and $item.DropDownItems) {
            Localize-MenuItems $item.DropDownItems
        }
    }
}
